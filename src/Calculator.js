// add function

const add = (a, b) => {
	return a + b;
};

// subtract function

const subtract = (a, b) => {
	return a - b;
};

// multiply function

const multiply = (a, b) => {
	return a * b;
};

// divide function

const divide = (a, b) => {
	return a / b;
};

export { add, subtract, multiply, divide };
